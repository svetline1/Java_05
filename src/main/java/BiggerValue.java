public class BiggerValue {

    /*Напишите алгоритм BiggerValue, который из двух параметров типа int возвращает бОльшее значение.
    Test Data:
            3333, 9999

     */

    public int biggerValue(int k, int l) {

       if (k >= l) {
           return k;
       } else {
           return l;
       }
    }



}
